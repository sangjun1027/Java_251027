package day1;

public class 데이터타입 {
	public static void main(String[] args) {
	// 반려견의 이름을 name 변수에 저장하세요
		String name = "멍멍";
		
	// 반려견의 나이를 age 변수에 저장하세요
		int age = 9;
		
	// 반려견의 몸무게를 weight 변수에 저장하세요
		double weight = 9.9;
		
		System.out.println("나의 반려견의 이름은 : " + name);
		System.out.println("나의 반려견의 나이는 : " + age);
		System.out.println("나의 반려견의 무게는 : " + weight);
	}
}
